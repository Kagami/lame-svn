<p>LAME was originally developed by Mike Cheng, from 1998 to 1999. After he
retired, the project started being maintained by Mark Taylor till early 2003.
After 2003, the project became managed by core developpers' teamwork, with no
official hierarchy or leadership.</p>

<p>The following list only represents some of the individuals (in alphabetical
order of family name) that contributed resources to LAME development. LAME owes
its quality and speed to contributions from many other people, including the
many people who post to the mp3encoder mailing list. See <a
href="http://lame.cvs.sourceforge.net/*checkout*/lame/lame/doc/html/history.html">
History</a> for more complete details.</p>

